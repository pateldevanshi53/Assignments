
//Write a simple program to print a multiplication table of a certain number taken from the user,
/*For eg. 2 X 1 = 2
 *        2 X 2 = 4
 *        and so on
 */



package task3;

public class T3_1 {

	public static void main(String[] args) {
		
        int a,b;
        for(a=1;a<=10;a++){
        	b=2*a;
        	System.out.println("2 x "+ a+ " = "+b);
        }
	}

}
